function showRestaurant(name) {
  // 모든 카드 그룹 숨김
  const allSections = ['kyungsulCards', 'esquareCards', 'gamsungCards', 'sevenCards', 'menuView'];
  allSections.forEach(id => document.getElementById(id)?.classList.add('d-none'));

  // 선택한 섹션만 표시
  if (name === 'kyungsul') {
    document.getElementById('kyungsulCards').classList.remove('d-none');
  } else if (name === 'esquare') {
    document.getElementById('esquareCards').classList.remove('d-none');
  } else if (name === 'gamsung') {
    document.getElementById('gamsungCards').classList.remove('d-none');
  } else if (name === 'seven') {
    document.getElementById('sevenCards').classList.remove('d-none');
  }

  history.replaceState(null, '', location.pathname); // 초기 상태로 리셋
}

function showMenu(id) {
  const menuData = menus[id];
  if (!menuData || !menuData.items) return;

  // 제목 및 설명 표시
  document.getElementById('menuTitle').textContent = menuData.title || '';
  document.getElementById('menuDescription').textContent = menuData.description || '';

  // 메뉴 카드 삽입
  const container = document.getElementById('menuCards');
  container.innerHTML = '';

  menuData.items.forEach(item => {
    const card = document.createElement('div');
    card.className = 'col';
    card.innerHTML = `
      <div class="card h-100">
        <img src="https://raw.githubusercontent.com/MCJE-HYEOK/WebProgrammingTest/refs/heads/main/images/${item.img}" class="card-img-top" alt="${item.name}">
        <div class="card-body">
          <h5 class="card-title">${item.name}</h5>
        </div>
      </div>`;
    container.appendChild(card);
  });

  // 카드 전환
  const allSections = ['kyungsulCards', 'esquareCards', 'gamsungCards', 'sevenCards'];
  allSections.forEach(id => document.getElementById(id)?.classList.add('d-none'));

  document.getElementById('menuView').classList.remove('d-none');

  // pushState로 상태 저장
  history.pushState({ view: 'menu' }, '', '#menu');
}

  // 뒤로가기 또는 이전 버튼 동작
function goBack() {
    history.back(); // 브라우저 히스토리 이동
}

  // popstate 이벤트: 사용자가 뒤로가기 시 실행됨
window.addEventListener('popstate', () => {
    showRestaurant('kyungsul');
});